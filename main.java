class Shamim{
    public static void main(String[] args) {
        int mark=90;
        switch (mark/10){
            case 9:
                System.out.println("Golden A+");
                break;
            case 8:
                System.out.println("A+");
                break;
            case 7:
                System.out.println("A");
                break;
            case 6:
                System.out.println("B");
                break;
            case 5:
                System.out.println("C");
                break;
            case 4:
                System.out.println("D");
                break;
            default:
                System.out.println("F");
                break;
        }

    }
}